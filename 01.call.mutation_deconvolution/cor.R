library(ggplot2)

data <- read.table("SBS.txt", header = TRUE, sep = "\t")

corr_test <- cor.test(data$SBS_contribution, data$Fraction, method = "spearman")
corr_coef <- corr_test$estimate
p_value <- corr_test$p.value

p <- ggplot(data, aes(x = Fraction, y = SBS_contribution)) + ## for EM-seq, fraction was replaced by sample overall methylation levels generated by Msuite2
  geom_point() +
  ggtitle("SBS vs Fraction") +
  xlab("Fraction") + ylab("SBS_contribution") +
  theme_minimal() +
  theme(panel.grid.major = element_blank(), 
        panel.grid.minor = element_blank(),
        panel.background = element_blank(),
	axis.ticks.x = element_line(color = "black", size = 0.2),
	axis.ticks.y = element_line(color = "black", size = 0.2),
        panel.border = element_rect(colour = "black", fill=NA)) 

label_text <- sprintf("R: %.2f, p: %g", corr_coef, p_value)
p <- p + annotate("text", x = mean(data$Fraction), y = max(data$SBS_contribution), label = label_text, vjust = 1.5, hjust = 0.5)

ggsave("scatter.pdf", plot = p, width = 6, height = 6, dpi = 300)
