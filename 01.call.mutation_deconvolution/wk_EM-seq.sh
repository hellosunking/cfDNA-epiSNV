#!/bin/bash
#
# This program is designed for EM-seq data process, including raw data process, call mutation and deconvolution
#
# Step 1: data process && call mutation
while read sid group
do
    sh EM_data_process.sh $sid

    sh EM_call_mutaion.sh $sid
    echo -ne "$sid\t$group\t$sid.final.vcf.gz\n" >> vcf.info
done < sample.info

# Step 2: mutation profiles analysis
Rscript MutationalPatterns_profile.R vcf.info final

perl normalize_matrix.pl final.mut_mat.xls > final.profile.txt

Rscript pca.R final.profile.txt sample.info sample

Rscript cluster.R final.profile.txt sample

# Step 3: mutation signatures deconvolution
Rscript deconvolution.R final.mut_mat.xls COSMIC.SBS.txt

perl get_SBS_contribution.pl SBS_sample_contribution_rate.txt | head -n 2 | awk '{for(i=1;i<=NF;i++)a[i]=a[i]?a[i]"\t"$i:$i}END{for(i=1;i<=NF;i++)print a[i]}' > SBS_contribution.txt

Rscript Fraction_of_COSMIC_box.R SBS_contribution.txt Fraction_of_COSMIC Fraction_of_COSMIC.pdf

perl -alne 'print if $F[0] !~ /Ctr/' SBS_sample_contribution_rate.txt | awk '{for(i=1;i<=NF;i++)a[i]=a[i]?a[i]"\t"$i:$i}END{for(i=1;i<=NF;i++)print a[i]}' > tumor_sbs.plot.txt

Rscript SBS_contribution_plot.R tumor_sbs.plot.txt SBS_contribution SBS_contribution.pdf

Rscript roc.R SBS_contribution.txt SBS_Contribution SBS_Contribution.roc.pdf

## Manually generated SBS.cor.txt including SBS_contribution and overall methylation levels of every tumor sample generated by Msuite2
Rscript cor.R
